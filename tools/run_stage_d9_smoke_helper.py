#!/usr/bin/env python3
"""Run Stage D9 OFF/ON entrypoint smoke and assert key no-op signals.

This helper wraps the real Sacred/SeqFormer entrypoint with fixed minimal smoke
settings and avoids fragile shell quoting for nested overrides.
"""

from __future__ import annotations

import argparse
import json
import math
import shutil
import subprocess
import sys
from pathlib import Path
from typing import Any


def _require_mapping(value: Any, *, field_name: str) -> dict[str, Any]:
    if not isinstance(value, dict):
        raise RuntimeError(f"{field_name} must be an object")
    return value


def _require_bool(value: Any, *, field_name: str) -> bool:
    if not isinstance(value, bool):
        raise RuntimeError(f"{field_name} must be a boolean")
    return value


def _require_str(value: Any, *, field_name: str) -> str:
    if not isinstance(value, str) or not value:
        raise RuntimeError(f"{field_name} must be a non-empty string")
    return value


def _require_number(value: Any, *, field_name: str) -> float:
    if isinstance(value, bool) or not isinstance(value, (int, float)):
        raise RuntimeError(f"{field_name} must be numeric")
    return float(value)


def _resolve_existing_path(candidates: list[Path]) -> Path:
    for candidate in candidates:
        if candidate.exists():
            return candidate
    rendered = ", ".join(str(x) for x in candidates)
    raise FileNotFoundError(f"none of the candidate paths exist: {rendered}")


